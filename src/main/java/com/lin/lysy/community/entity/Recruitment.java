package com.lin.lysy.community.entity;

public class Recruitment {
    private String Com_Id;
    private String Recru_content;
    private String Recru_Id;
    private int Recru_number;
    private String Recru_title;
    private String Recru_date;
    private String Recru_time;

    public String getCom_Id() {
        return Com_Id;
    }

    public void setCom_Id(String com_Id) {
        Com_Id = com_Id;
    }

    public String getRecru_content() {
        return Recru_content;
    }

    public void setRecru_content(String recru_content) {
        Recru_content = recru_content;
    }

    public String getRecru_Id() {
        return Recru_Id;
    }

    public void setRecru_Id(String recru_Id) {
        Recru_Id = recru_Id;
    }

    public int getRecru_number() {
        return Recru_number;
    }

    public void setRecru_number(int recru_number) {
        Recru_number = recru_number;
    }

    public String getRecru_title() {
        return Recru_title;
    }

    public void setRecru_title(String recru_title) {
        Recru_title = recru_title;
    }

    public String getRecru_date() {
        return Recru_date;
    }

    public void setRecru_date(String recru_date) {
        Recru_date = recru_date;
    }

    public String getRecru_time() {
        return Recru_time;
    }

    public void setRecru_time(String recru_time) {
        Recru_time = recru_time;
    }
}
